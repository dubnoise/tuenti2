    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $messages = Message::select('messages.*', 'users.name')
        ->where('user_id_2', auth()->id())
        ->join('users', 'user_id', '=', 'users.id')
        ->get();

        $users = User::all();

        return view ('messages.index', compact('messages', 'users'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $messages = Message::all();
        $users = User::select('users.*', 'users.name')
        ->where('id', $_GET['id'])
        ->get();
        return view('messages.create', compact('messages', 'users'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $message = new Message();
        $message->content = $request->get('content');
        $message->user_id = $request->get('user_id');
        $message->user_id_2 = $request->get('user_id_2');
        $message->save();

        return view ('messages.show', compact('message'));
    }

